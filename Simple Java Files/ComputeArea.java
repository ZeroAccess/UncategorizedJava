public class ComputeArea {
public static void main(String[] args) {
//Step 1 Read in radius
double radius;
double area;

radius = 20;

//Step 2 Compute Area

area = radius * radius * 3.14159;
//Step 3 Display the Area
System.out.println("The area for the circle of radius " + radius + " is : " +area +".");
}
}